def bulk_items_for_migration(ExamMathItem):
    """供迁移 RunPython 调用：返回待 bulk_create 的模型实例列表。"""
    out = []
    for track, rows in (("m2", M2_ROWS), ("m1", M1_ROWS)):
        n = 0
        for subject, module, label in rows:
            n += 1
            out.append(
                ExamMathItem(
                    track=track,
                    subject=subject,
                    module=module,
                    label=label,
                    sort_order=n,
                    owner_id=None,
                )
            )
    return out


def unseed_builtin(ExamMathItem):
    ExamMathItem.objects.filter(owner_id__isnull=True).delete()
